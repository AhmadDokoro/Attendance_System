package Model;

public class Attendance {
    private int attendanceId;
    private int studentId;
    private int sessionId;
    private String scanTime;
    private String ipAddress;
    private String markedByLecturer;
    
    
    //for SessionAttendanceListDao sake
    private String studentName;
    private String matric;

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getMatric() {
        return matric;
    }

    public void setMatric(String matric) {
        this.matric = matric;
    }

    
    // Getters and Setters
    public int getAttendanceId() {
        return attendanceId;
    }

    public void setAttendanceId(int attendanceId) {
        this.attendanceId = attendanceId;
    }

    public int getStudentId() {
        return studentId;
    }

    public void setStudentId(int studentId) {
        this.studentId = studentId;
    }

    public int getSessionId() {
        return sessionId;
    }

    public void setSessionId(int sessionId) {
        this.sessionId = sessionId;
    }

    public String getScanTime() {
        return scanTime;
    }

    public void setScanTime(String scanTime) {
        this.scanTime = scanTime;
    }

    public String getIpAddress() {
        return ipAddress;
    }

    public void setIpAddress(String ipAddress) {
        this.ipAddress = ipAddress;
    }

    public String getMarkedByLecturer() {
        return markedByLecturer;
    }

    public void setMarkedByLecturer(String markedByLecturer) {
        this.markedByLecturer = markedByLecturer;
    }
}

